    private void uploadButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uploadButtonActionPerformed
        final JFileChooser fileChooser = new JFileChooser();
        fileChooser.setBackground(Color.white);
        fileChooser.setForeground(Color.red);
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        fileChooser.setApproveButtonText("Upload");
        int isFileSelected = fileChooser.showOpenDialog(RemoteFileStorageView.this);

        if (isFileSelected == JFileChooser.APPROVE_OPTION) {
            java.io.File file = fileChooser.getSelectedFile();

            try {
                RemtoteFileStorageClient.rfsReference.upload(file.getName(), file.length(), FileUtils.fileToByte(file.getAbsolutePath()));

                updatePanel();

//                showSampleProgressBar();
            } catch (RemoteException ex) {
                Logger.getLogger(RemoteFileStorageView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_uploadButtonActionPerformed


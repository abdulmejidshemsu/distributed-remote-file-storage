    private void updatePanel() {
        switch (currentPanel) {
            case "dashboardPanel":
                dashboardViewBtnActionPerformed(null);
                break;

            case "allFilePanel":
                allFileViewBtnActionPerformed(null);
                break;

            case "fileWithExtensionPanel":
                fetchWithExtension(this.currentSelectedExtension);
                break;

            case "searchFilePanel":
                searchFileInputActionPerformed(null);
                break;

            case "imagePanel":
                imageViewBtnActionPerformed(null);
                break;

            case "videoPanel":
                videoViewBtnActionPerformed(null);
                break;

            case "favoritePanel":
                favoriteViewBtnActionPerformed(null);
                break;

            case "audioPanel":
                audioViewBtnActionPerformed(null);
                break;
        }
    }

    private void showSampleProgressBar() {
        JDialog progressDialog = new JDialog();

        progressDialog.setTitle("Uploading");
        progressDialog.setSize(300, 300);
        progressDialog.setLocationRelativeTo(this);
        progressDialog.setModal(true);
        progressDialog.setVisible(true);

        try {
            Thread.sleep(1000);
        } catch (InterruptedException ex) {
            Logger.getLogger(RemoteFileStorageView.class.getName()).log(Level.SEVERE, null, ex);
        }

        progressDialog.setVisible(false);
        progressDialog.dispose();
    }

    private void setDashboard(Dashboard dashboard) {
        imageCardText.setText(dashboard.getTotalImagesCount() + " Items / " + FileUtils.humanReadableByte((long) dashboard.getTotalImagesSize()));
        videoCardText.setText(dashboard.getTotalVideosCount() + " Items / " + FileUtils.humanReadableByte((long) dashboard.getTotalVideosSize()));
        audioCardText.setText(dashboard.getTotalAudiosCount() + " Items / " + FileUtils.humanReadableByte((long) dashboard.getTotalAudiosSize()));
        documentCardText.setText(dashboard.getTotalDocumentsCount() + " Items / " + FileUtils.humanReadableByte((long) dashboard.getTotalDocumentsSize()));

        recentFilesPanel.setLayout(new BoxLayout(recentFilesPanel, BoxLayout.PAGE_AXIS));
        recentFilesPanel.removeAll();
        recentFilesPanel.repaint();
        recentFilesPanel.revalidate();

        Iterator recentFilesIter = dashboard.getRecentFiles().iterator();
        while (recentFilesIter.hasNext()) {
            recentFilesPanel.add(new ListTilePanel((File) recentFilesIter.next()));
        }

        recentFilesPanel.repaint();
        recentFilesPanel.revalidate();

    }

    private void setFilePanel(JPanel panel, ArrayList<File> files, String currentPanel) {
        this.changePanel(panel, currentPanel);

        panel.setLayout(new FlowLayout(FlowLayout.LEFT, 55, 55));
        panel.removeAll();
        panel.repaint();
        panel.revalidate();

        if (files.isEmpty()) {
            panel.add(new RecordEmptyPanel());
        } else {
            Iterator panelIter = files.iterator();
            while (panelIter.hasNext()) {
                panel.add(new FileCard((File) panelIter.next(), currentPanel));
            }
        }

        panel.repaint();
        panel.revalidate();
    }

    private void showFilesPanel(ArrayList<File> files, String currentPanel) {
        showFilesScrollPanel.setLayout(new FlowLayout(FlowLayout.LEFT, 55, 55));
        showFilesScrollPanel.removeAll();
        showFilesScrollPanel.repaint();
        showFilesScrollPanel.revalidate();

        if (files.isEmpty()) {
            showFilesScrollPanel.add(new RecordEmptyPanel());
        } else {
            Iterator panelIter = files.iterator();
            while (panelIter.hasNext()) {
                showFilesScrollPanel.add(new FileCard((File) panelIter.next(), currentPanel));
            }
        }

        showFilesScrollPanel.repaint();
        showFilesScrollPanel.revalidate();
        this.changePanel(showFilesPanel, currentPanel);
    }

